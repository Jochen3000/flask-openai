def convert_to_detokenized_text(tokenized_text):
    prompt_text = " ".join(tokenized_text)
    prompt_text = prompt_text.replace(" 's", "'s")
    prompt_text = prompt_text.replace(" 's", "'s")
    prompt_text = prompt_text.replace("\n", " ")
    return prompt_text

